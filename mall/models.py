    def pending_items(self):
        return self.items.filter(status='pending').count()

    def refresh_status(self):
        """Recalculate counts and overall batch status."""
        total = self.items.count()
        approved = self.items.filter(status='approved').count()
        rejected = self.items.filter(status='rejected').count()
        if total == 0:
            status = 'rejected' if self.rejected_items else 'pending'
        elif approved == total:
            status = 'approved'
        elif rejected == total:
            status = 'rejected'
        elif approved > 0 or rejected > 0:
            status = 'partial'
        else:
            status = 'pending'

        update_fields = []
        if self.total_items != total:
            self.total_items = total
            update_fields.append('total_items')
        if self.approved_items != approved:
            self.approved_items = approved
            update_fields.append('approved_items')
        if self.rejected_items != rejected:
            self.rejected_items = rejected
            update_fields.append('rejected_items')
        if self.status != status:
            self.status = status
            update_fields.append('status')
        if update_fields:
            self.save(update_fields=update_fields)


