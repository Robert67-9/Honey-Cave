# ─── Chain of Custody — Handoff Codes ─────────────────────────────────────────

class HandoffCode(models.Model):
    """
    Tracks one handoff in the chain-of-custody flow for an order.

    Stages (delivery order):
        admin_to_keeper       Admin → Fulfillment Officer. Admin generates code, fulfillment officer enters it.
        keeper_to_rider       Fulfillment Officer → Rider. Fulfillment Officer auto-issues code, rider enters it.
        rider_to_customer     Rider → Customer. Rider shows code, customer enters it.

    Stages (pickup order — no rider):
        admin_to_keeper       Same as above.
        keeper_to_customer    Fulfillment Officer → Customer. Fulfillment Officer shows code, customer types it on collection.

    Lifecycle:
        Generated  → code is issued, sent via in-app/WhatsApp/SMS
        Verified   → receiver entered correct code, used_at is set
        Locked     → 3 wrong attempts, admin alerted, used_at stays null
        Expired    → 10 minutes passed without verification (admin can regenerate)
    """
    STAGE_CHOICES = [
        ('admin_to_officer',   'Admin → Fulfillment Officer'),
        ('officer_to_rider',   'Fulfillment Officer → Rider'),
        ('rider_to_customer', 'Rider → Customer'),
        ('officer_to_customer','Fulfillment Officer → Customer (Pickup)'),
    ]
    EXPIRY_MINUTES = 120   # 2 hours — enough for real-world shop operations
    MAX_ATTEMPTS = 3

    order        = models.ForeignKey('Order', on_delete=models.CASCADE, related_name='handoff_codes')
    stage        = models.CharField(max_length=24, choices=STAGE_CHOICES)
    code         = models.CharField(max_length=6, db_index=True,
                                    help_text='6-digit numeric code shown to the issuer and entered by the receiver.')
    attempts     = models.PositiveIntegerField(default=0)
    locked       = models.BooleanField(default=False,
                                       help_text='Set to True after MAX_ATTEMPTS wrong tries. Requires admin unlock.')
    created_at   = models.DateTimeField(auto_now_add=True)
    used_at      = models.DateTimeField(null=True, blank=True)
    used_by      = models.ForeignKey(User, on_delete=models.SET_NULL, null=True, blank=True,
                                     related_name='handoff_codes_used',
                                     help_text='User who entered the correct code (if logged in).')
    issued_to_label = models.CharField(max_length=120, blank=True, default='',
                                       help_text='Free-text label for who the code was sent to (e.g. rider name, customer name) — useful when no User account exists.')

    class Meta:
        ordering = ['-created_at']
        indexes = [
            models.Index(fields=['order', 'stage']),
        ]

    def __str__(self):
        return f'{self.get_stage_display()} for Order {self.order_id} ({self.code})'

    @property
    def is_expired(self):
        if self.used_at:
            return False
        return (timezone.now() - self.created_at).total_seconds() > self.EXPIRY_MINUTES * 60

    @property
    def is_verified(self):
        return self.used_at is not None

    @property
    def is_active(self):
        """True when the code can still be used to verify a handoff."""
        return not self.locked and not self.is_verified and not self.is_expired

    @property
    def remaining_attempts(self):
        return max(0, self.MAX_ATTEMPTS - self.attempts)

    @property
    def seconds_until_expiry(self):
        if self.used_at:
            return 0
        delta = (self.created_at + timedelta(minutes=self.EXPIRY_MINUTES)) - timezone.now()
        return max(0, int(delta.total_seconds()))

    @property
    def qr_svg(self):
        """Inline SVG QR code encoding `self.code` — for templates."""
        # Lazy import to avoid loading qrcode at module-import time.
        from .handoff import qr_svg_for_code
        return qr_svg_for_code(self.code)


