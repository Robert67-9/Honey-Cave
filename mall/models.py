    available = models.BooleanField(default=True)
    created = models.DateTimeField(auto_now_add=True)
    updated = models.DateTimeField(auto_now=True)
    # Who uploaded this product (admin or fulfillment officer). Lets the
    # officer portal show "My Products" — everything this officer added.
    created_by = models.ForeignKey(
        User, on_delete=models.SET_NULL, null=True, blank=True,
        related_name='uploaded_products',
        help_text='User (admin or officer) who added this product.',
    )
    
    def __str__(self):
        return self.name

    def avg_rating(self):
        """Average rating across approved reviews. Returns float or None."""
        from django.db.models import Avg
        result = self.reviews.filter(is_approved=True).aggregate(Avg('rating'))['rating__avg']
        return round(result, 1) if result else None

    def review_count(self):
        return self.reviews.filter(is_approved=True).count()

    def rating_breakdown(self):
        """Returns {5:%, 4:%, 3:%, 2:%, 1:%} for the rating bar chart."""
        from django.db.models import Count
        total = self.review_count()
        if not total:
            return {i: 0 for i in range(5, 0, -1)}
        counts = {
            r['rating']: r['count']
            for r in self.reviews.filter(is_approved=True).values('rating').annotate(count=Count('rating'))
        }
        return {i: round(counts.get(i, 0) / total * 100) for i in range(5, 0, -1)}

    # ── Per-branch pricing helpers ──────────────────────────────────────────
    def price_at_branch(self, branch):
        """
        Return the selling price for this product at `branch`, or None if
        the branch doesn't carry it. Falls back to None (NOT self.price)
        because per the strict-stock policy, no row means "not sold here".
        """
        if branch is None:
            return self.price
        bp = self.branch_products.filter(branch=branch, is_available=True).first()
        return bp.price if bp else None

    def stock_at_branch(self, branch):
        """Return per-branch stock, or 0 if not carried."""
        if branch is None:
            return self.stock  # legacy single-stock fallback for un-branched flows
        bp = self.branch_products.filter(branch=branch, is_available=True).first()
        return bp.stock if bp else 0

    def is_available_at_branch(self, branch):
        """True if `branch` carries this product and it's marked available."""
        if branch is None:
            return self.available
        return self.branch_products.filter(branch=branch, is_available=True).exists()

    def branches_carrying(self):
        """Queryset of branches that currently carry this product (in stock + available)."""
        from django.db.models import F
        return (self.branch_products
                .filter(is_available=True, stock__gt=0)
                .select_related('branch')
                .order_by('branch__name'))

