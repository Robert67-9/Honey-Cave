class PromoCode(models.Model):
    DISCOUNT_TYPE_CHOICES = [
        ('percent', 'Percentage (%)'),
        ('fixed',   'Fixed Amount (GH₵)'),
    ]
    code           = models.CharField(max_length=50, unique=True)
    discount_type  = models.CharField(max_length=10, choices=DISCOUNT_TYPE_CHOICES, default='percent')
    discount_value = models.DecimalField(max_digits=8, decimal_places=2,
                                         help_text='Percentage (0–100) or fixed GH₵ amount')
    min_order_value = models.DecimalField(max_digits=8, decimal_places=2, default=Decimal('0'),
                                          help_text='Minimum cart subtotal to activate this code')
    max_uses       = models.PositiveIntegerField(null=True, blank=True,
                                                  help_text='Leave blank for unlimited uses')
    times_used     = models.PositiveIntegerField(default=0)
    valid_from     = models.DateTimeField(default=timezone.now)
    valid_until    = models.DateTimeField(null=True, blank=True)
    is_active      = models.BooleanField(default=True)
    created        = models.DateTimeField(auto_now_add=True)

    def __str__(self):
        return f'{self.code} ({self.get_discount_type_display()} — {self.discount_value})'

    def is_valid(self):
        now = timezone.now()
        if not self.is_active:
            return False, 'This promo code is inactive.'
        if now < self.valid_from:
            return False, 'This promo code is not yet active.'
        if self.valid_until and now > self.valid_until:
            return False, 'This promo code has expired.'
        if self.max_uses is not None and self.times_used >= self.max_uses:
            return False, 'This promo code has reached its usage limit.'
        return True, None

    def calculate_discount(self, subtotal: Decimal) -> Decimal:
        """Return the discount amount to subtract from subtotal."""
        if subtotal < self.min_order_value:
            return Decimal('0')
        if self.discount_type == 'percent':
            return (subtotal * self.discount_value / 100).quantize(Decimal('0.01'))
        return min(self.discount_value, subtotal)


