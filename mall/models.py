class RiderOTP(models.Model):
    """
    One-time password for rider phone-login.

    The plaintext code is sent to the rider's phone via WhatsApp + SMS.
    Only the SHA-256 hash is stored on the row, so a database leak doesn't
    let attackers reuse outstanding OTPs.

    A new OTP supersedes any older unused OTPs for the same rider so they
    don't accumulate. attempts is incremented on each wrong-code submit;
    after MAX_ATTEMPTS the row locks and the rider must request a new code.
    """
    EXPIRY_MINUTES = 10
    MAX_ATTEMPTS   = 3

    rider        = models.ForeignKey('Rider', on_delete=models.CASCADE, related_name='otps')
    code_hash    = models.CharField(max_length=64, db_index=True,
                                    help_text='SHA-256 of the 6-digit code; never store the plaintext.')
    expires_at   = models.DateTimeField()
    attempts     = models.PositiveSmallIntegerField(default=0)
    used_at      = models.DateTimeField(null=True, blank=True)
    locked       = models.BooleanField(default=False)
    requested_ip = models.GenericIPAddressField(null=True, blank=True)
    created_at   = models.DateTimeField(auto_now_add=True)

    class Meta:
        ordering = ['-created_at']
        indexes  = [
            models.Index(fields=['rider', 'used_at']),
        ]

    def __str__(self):
        state = 'used' if self.used_at else ('locked' if self.locked else 'pending')
        return f'OTP for {self.rider.phone} ({state})'

    @property
    def is_expired(self):
        from django.utils import timezone as _tz
        return _tz.now() >= self.expires_at

    @property
    def remaining_attempts(self):
        return max(0, self.MAX_ATTEMPTS - self.attempts)

    @classmethod
    def hash_code(cls, code):
        """Hash a plaintext OTP for comparison/storage."""
        import hashlib as _h
        return _h.sha256(code.encode('utf-8')).hexdigest()


class RiderSession(models.Model):
    """
    Authenticated session for a logged-in rider.

    Riders aren't Django Users (they don't have usernames or passwords),
    so we run our own session table. The session token is stored in a
    signed httponly cookie on the rider's phone; this row records the
    binding plus metadata for "active devices" management.

    revoked=True means the session is invalid — used for logout and for
    admin "kick this device". expired sessions are kept until cleaned up
    by a periodic task (so admins can audit recent activity).
    """
    DEFAULT_TTL_DAYS = 30   # "trust this device for 30 days"
    TOKEN_BYTES      = 32   # → 64 hex chars; secrets.token_hex(32)

    rider         = models.ForeignKey('Rider', on_delete=models.CASCADE, related_name='sessions')
    token         = models.CharField(max_length=64, unique=True, editable=False, db_index=True)
    expires_at    = models.DateTimeField(db_index=True)
    last_used_at  = models.DateTimeField(auto_now=True)
    user_agent    = models.CharField(max_length=300, blank=True)
    ip_address    = models.GenericIPAddressField(null=True, blank=True)
    revoked       = models.BooleanField(default=False)
    created_at    = models.DateTimeField(auto_now_add=True)

    class Meta:
        ordering = ['-last_used_at']
        indexes  = [
            models.Index(fields=['rider', 'revoked', 'expires_at']),
        ]

    def __str__(self):
        return f'Session for {self.rider.name} ({self.token[:8]}…)'

    @property
    def is_active(self):
        from django.utils import timezone as _tz
        return not self.revoked and _tz.now() < self.expires_at

    def save(self, *args, **kwargs):
        if not self.token:
            import secrets as _s
            self.token = _s.token_hex(self.TOKEN_BYTES)
        if not self.expires_at:
            from django.utils import timezone as _tz
            from datetime import timedelta as _td
            self.expires_at = _tz.now() + _td(days=self.DEFAULT_TTL_DAYS)
        super().save(*args, **kwargs)


