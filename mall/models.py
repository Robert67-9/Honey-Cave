class StockReservation(models.Model):
    """
    Holds stock for a session while the customer is at checkout.
    Released automatically after RESERVATION_MINUTES if not converted to an order.
    Created when a user loads the checkout page; deleted on order placement or expiry.
    """
    RESERVATION_MINUTES = 15

    session_key = models.CharField(max_length=40, db_index=True)
    product     = models.ForeignKey(Product, on_delete=models.CASCADE, related_name='reservations')
    quantity    = models.PositiveIntegerField()
    expires_at  = models.DateTimeField(db_index=True)
    created_at  = models.DateTimeField(auto_now_add=True)

    class Meta:
        unique_together = ('session_key', 'product')

    def __str__(self):
        return f'Reservation {self.session_key[:8]}… — {self.quantity}× {self.product.name}'

    @classmethod
    def expire_old(cls):
        """Release expired reservations back to stock. Call from checkout view."""
        from django.utils import timezone as _tz
        from django.db.models import F as _F
        expired = cls.objects.filter(expires_at__lt=_tz.now()).select_related('product')
        for res in expired:
            Product.objects.filter(pk=res.product_id).update(
                stock=_F('stock') + res.quantity
            )
        expired.delete()

    @classmethod
    def release_for_session(cls, session_key):
        """Release all reservations for a session (on order placed or cart cleared)."""
        from django.db.models import F as _F
        reservations = cls.objects.filter(session_key=session_key).select_related('product')
        for res in reservations:
            Product.objects.filter(pk=res.product_id).update(
                stock=_F('stock') + res.quantity
            )
        reservations.delete()


