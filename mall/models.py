    # an exact pin instead of a fuzzy address search. Optional — not all
    # devices/permissions allow it; we always fall back to address text.
    delivery_lat      = models.FloatField(null=True, blank=True)
    delivery_lng      = models.FloatField(null=True, blank=True)
    paid               = models.BooleanField(default=False)
    payment_reference  = models.CharField(max_length=100, blank=True, default='', help_text='Paystack/bank transfer reference')
    # FEAT-07: Promo code applied at checkout
