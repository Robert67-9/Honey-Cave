    @property
    def order_number(self):
        """Human-readable order number: #HCM000001, #HCM000042, etc."""
        return f'#HCM{self.id:06d}'

    def get_region_display_name(self):
        return dict(REGION_CHOICES).get(self.region, self.region)

    @property
    def maps_directions_url(self):
        """
        Return a Google Maps URL the rider can tap to start turn-by-turn
        navigation. Uses the GPS pin when available — that's the only way
        to actually get to a Ghanaian house, since most addresses are
        descriptive ("behind the blue tank") rather than geocodable.

        Falls back to a Google Maps SEARCH on the typed address if no GPS
        was captured. Better than nothing, but accuracy depends entirely
        on how Google parses the customer's address text.
        """
        from urllib.parse import quote_plus
        if self.delivery_lat is not None and self.delivery_lng is not None:
            # Directions API deep-link: opens Google Maps app on mobile,
            # web Maps on desktop. travelmode=driving is the right default
            # for riders on motorbikes/cars.
            return (
                f'https://www.google.com/maps/dir/?api=1'
                f'&destination={self.delivery_lat},{self.delivery_lng}'
                f'&travelmode=driving'
            )
        # Fallback: search by typed address + city. Google will best-guess.
        addr = (self.delivery_address or self.address or '').strip()
        if not addr:
            return ''
        query = f'{addr}, {self.city}' if self.city else addr
        return f'https://www.google.com/maps/search/?api=1&query={quote_plus(query)}'

    @property
    def has_gps_pin(self):
        return self.delivery_lat is not None and self.delivery_lng is not None

    def subtotal(self):
        # BUG-03 FIX: Compute subtotal directly from order items so this method
        # is correct even when called before calculate_total() has run (e.g. in
        # unit tests or admin edits). Avoids returning a negative when total_price=0.
        items_total = sum(item.get_total_price() for item in self.items.all())
        if items_total:
            return items_total
        # Fallback for orders where items may have been deleted
        return max(Decimal('0'), self.total_price - self.shipping_fee)

    def calculate_total(self):
        """
        Recompute total_price from the items + shipping_fee, applying any
        promo discount that's been recorded on the order.

        Used by admin tools that edit orders after creation. The original
        checkout flow sets total_price directly from the server-computed
        grand total (which already includes the discount), so this method's
        main job is to keep things consistent if anyone re-runs it later.
        """
        from decimal import Decimal as _D
        subtotal = sum(item.get_total_price() for item in self.items.all())
        self.shipping_fee = REGION_FEES.get(self.region, _D('15.00'))
        # Subtract any promo discount; floor at zero so a generous promo
        # can't produce a negative total.
        discount = self.discount_amount or _D('0')
        self.total_price = max(_D('0'), subtotal + self.shipping_fee - discount)
        self.save()


