# ─── Delivery fee calculation ─────────────────────────────────────────────────

# Base fee for all home deliveries (covers first 5 km)
DELIVERY_BASE_FEE   = Decimal('10.00')   # GH₵ 10 base
DELIVERY_RATE_PER_KM = Decimal('1.50')   # GH₵ 1.50 per km after 5 km
DELIVERY_FREE_KM    = 5                  # first 5 km included in base fee
DELIVERY_MAX_FEE    = Decimal('80.00')   # cap for very long distances

# Estimated delivery time: 20 min base + 3 min per km
DELIVERY_BASE_MINS  = 20
DELIVERY_MINS_PER_KM = 3


def calculate_delivery_fee(distance_km: float):
    """
    Return (fee: Decimal, eta_minutes: int) for a home delivery.
    distance_km is the straight-line distance from branch to delivery address.
    Uses actual road distance approximation (×1.3 factor for road detours).
    """
    road_km = distance_km * 1.3   # straight-line → estimated road distance
    if road_km <= DELIVERY_FREE_KM:
        fee = DELIVERY_BASE_FEE
    else:
        extra_km = road_km - DELIVERY_FREE_KM
        fee = DELIVERY_BASE_FEE + (Decimal(str(round(extra_km, 2))) * DELIVERY_RATE_PER_KM)
    fee = min(fee, DELIVERY_MAX_FEE).quantize(Decimal('0.01'))
    eta = int(DELIVERY_BASE_MINS + road_km * DELIVERY_MINS_PER_KM)
    return fee, eta


