class AdminTOTP(models.Model):
    """
    TOTP secret for admin 2FA. One record per staff user.
    The secret is base32-encoded and compatible with Google Authenticator,
    Authy, and any RFC 6238 TOTP app.
    """
    user       = models.OneToOneField(User, on_delete=models.CASCADE, related_name='totp')
    secret     = models.CharField(max_length=64)
    is_enabled = models.BooleanField(default=False)
    created_at = models.DateTimeField(auto_now_add=True)

    def __str__(self):
        return f'TOTP for {self.user.username} ({"on" if self.is_enabled else "off"})'

    def verify(self, token: str) -> bool:
        """Verify a 6-digit TOTP token. Allows ±1 window for clock skew."""
        try:
            import pyotp
            return pyotp.TOTP(self.secret).verify(token.strip(), valid_window=1)
        except Exception:
            return False

    @classmethod
    def get_or_create_secret(cls, user):
        """Return existing TOTP record or create one with a fresh secret."""
        import pyotp
        obj, created = cls.objects.get_or_create(
            user=user,
            defaults={'secret': pyotp.random_base32()},
        )
        return obj


