def normalize_phone(raw):
    """Normalize phone numbers to digits-only E.164-like form."""
    if not raw:
        return ''
    digits = ''.join(ch for ch in str(raw) if ch.isdigit())
    if digits.startswith('0') and len(digits) == 10:
        digits = '233' + digits[1:]
    return digits


def generate_otp():
    return ''.join(random.choices(string.digits, k=6))


class OTPVerification(models.Model):
    """
    One-time password for email-verification (signup) and password-reset.

    SECURITY: only the SHA-256 hash of the code is stored on the row.
    Plaintext is generated by `create_for()`, sent to the user immediately,
    and then discarded. A DB read leak therefore can't yield active codes.

    Migrating from the older plaintext-`code` schema is handled by the
    accompanying migration which:
       - adds `code_hash` populated from sha256(code) for existing rows
       - keeps `code` for one release cycle as a fallback for any in-flight
         OTPs created before deploy (older rows still verify either way)
       - drops `code` in a follow-up migration once we're sure no plaintext
         OTPs are flying around
    """
    OTP_PURPOSE_CHOICES = [
        ('signup', 'Sign Up Verification'),
        ('password_reset', 'Password Reset'),
    ]
    user        = models.ForeignKey(User, on_delete=models.CASCADE, related_name='otps')
    code        = models.CharField(max_length=6, blank=True, default='',
                                   help_text='DEPRECATED: kept for backward-compat with pre-hash rows. New rows leave this empty.')
    code_hash   = models.CharField(max_length=64, blank=True, default='',
                                   db_index=True,
                                   help_text='SHA-256 of the 6-digit OTP. Authoritative.')
    purpose     = models.CharField(max_length=20, choices=OTP_PURPOSE_CHOICES, default='signup')
    is_used     = models.BooleanField(default=False)
    created_at  = models.DateTimeField(auto_now_add=True)

    def is_valid(self):
        """OTP is valid for 10 minutes."""
        return not self.is_used and timezone.now() < self.created_at + timedelta(minutes=10)

    def __str__(self):
        return f'OTP({self.purpose}) for {self.user.username}'

    @staticmethod
    def hash_code(code):
        """SHA-256 hex digest of a plaintext code. Used at create + verify time."""
        import hashlib as _h
        return _h.sha256(code.encode('utf-8')).hexdigest()

    def matches(self, entered_code):
        """
        Constant-time check that an entered OTP matches this row.
        Prefers code_hash; falls back to plaintext `code` for legacy rows
        created before the hash migration. New rows leave `code` empty so
        the fallback only fires for in-flight pre-deploy OTPs.
        """
        from django.utils.crypto import constant_time_compare
        entered = (entered_code or '').strip()
        if not entered:
            return False
        if self.code_hash:
            return constant_time_compare(self.hash_code(entered), self.code_hash)
        if self.code:
            # Legacy plaintext — accepted only until those rows expire (10 min).
            return constant_time_compare(self.code, entered)
        return False

    @classmethod
    def create_for(cls, user, purpose='signup'):
        """
        Generate a fresh OTP for `user`. Returns (otp_row, plaintext_code).
        Caller is expected to immediately deliver `plaintext_code` via
        send_otp() and then discard it — the row only stores the hash.
        """
        plaintext = generate_otp()
        row = cls.objects.create(
            user=user,
            purpose=purpose,
            code_hash=cls.hash_code(plaintext),
            # `code` left blank — never persist plaintext on new rows.
        )
        return row, plaintext


